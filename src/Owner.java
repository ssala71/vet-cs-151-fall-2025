public class Owner {
    // Attributes
    private String name;
    private String phoneNumber;
    private String email;
    private String address;
    private int age;
    private String gender;

    // Constructor
    public Owner(String name, String phoneNumber, String email, String address, int age, String gender){
        this.name = name;
        this.phoneNumber = phoneNumber;
        this.email = email;
        this.address = address;
        this.age = age;
        this.gender = gender;   
    }
}
